    void fillTable() {
        String sql = "select * from MaKhuyenMai where promocode like ? and status =1";
        List<MaKhuyenMai> list = dao.selectBySql(sql, "%" + txtTim.getText() + "%");
        DefaultTableModel model = (DefaultTableModel) tblKhuyenMai.getModel();
        model.setRowCount(0);
        for (MaKhuyenMai x : list) {
            model.addRow(new Object[]{x.getPromoCode(), x.getValue()});
        }
    }
