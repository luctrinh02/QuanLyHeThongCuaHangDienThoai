        model.setRowCount(0);
        List<Object[]>list=tk.thongKeKhachHang();
        for(Object[] x:list){
            model.addRow(new Object[]{x[0],x[1],Xmoney.moneyToString(Double.parseDouble(x[2]+"")),x[3]});
        }
